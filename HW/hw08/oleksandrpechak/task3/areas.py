def area_of_rectangle (w, h):
    """
    Calculate the area of a rectangle
    """
    return w*h
def area_of_triangle(b, h):
    """
    Calculate the area of a triangle
    """
    return 1/2*b*h
def area_of_circle(r):
    """
    Calculate the are of a circle
    """
    import math
    return math.pi*pow(r,2)